package hafta4uygulama1;
public class Bul {
    public double alanBul(int kenar1, int kenar2){
        return kenar1 * kenar2;
    }
    public double alanBul(double kenar1, double kenar2){
        return kenar1 * kenar2;
    }
    
    public double cevreBul(int kenar1, int kenar2){
        return (kenar1 + kenar2) * 2;
    }
    public double cevreBul(double kenar1, double kenar2){
        return (kenar1 + kenar2) * 2;
    }

}
